function showMore1(){
  if(document.getElementById("hid1").style.display == "none"){
    document.getElementById("hid1").style.display = "table";
    document.getElementById('daplus1').src = "minus.png";
    document.getElementById('daplus1').style.paddingTop = "15px";
  }
  else{
    document.getElementById("hid1").style.display = "none";
    document.getElementById('daplus1').src = "plus.png";
    document.getElementById('daplus1').style.paddingTop = "0";
  }
}

